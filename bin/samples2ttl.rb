#!/usr/bin/env ruby
# frozen_string_literal: true
#
# samples2ttl.rb — JoGo の sample 情報を取得し、集団 ttl と合わせて output に出力する
#
#   API: https://jogo.csml.org/api/v1/samples
#        cursor 方式ページング (limit 最大1000, meta.pagination.next_cursor)
#        各 sample: {sampleid, popname, gpopname, groupnames[], source_table, ...}
#
# 使い方 (プロジェクト直下から):
#   ruby bin/samples2ttl.rb                          # templates/population.ttl + sample → output/samples.ttl
#   ruby bin/samples2ttl.rb --template T --out O     # 入出力を指定
#   ruby bin/samples2ttl.rb --out -                  # 標準出力
#
# 動作:
#   - 集団 ttl (--template) を読み、全 sample を jogo:Sample として末尾に足して --out に書く。
#     出力 output/samples.ttl は「集団 + sample」の完全版(ロード時はこれ単体で閉じる)。
#   - sample が参照する集団/スーパー集団のうち template に未定義のコードには stub を補う。
#   - sample ブロックは下記マーカーで囲む。
#       # === GENERATED by samples2ttl.rb : DO NOT EDIT BELOW ===
#       ...
#       # === END GENERATED (samples2ttl.rb) ===

require 'json'
require 'net/http'
require 'uri'
require 'set'
require 'optparse'

SAMPLES_API = 'https://jogo.csml.org/api/v1/samples'
# groupnames にこれらのいずれかを含む sample だけを対象にする
KEEP_GROUPS = ['JoGo_JPT_HPRC', 'JoGo_JPT'].freeze
SAMPLE_PREFIX = '@prefix jogo_sample: <http://jogo.csml.org/rdf/sample/> .'
MARK_BEGIN = '# === GENERATED by samples2ttl.rb ==='
MARK_END   = '# === END GENERATED (samples2ttl.rb) ==='

module Samples
  module_function

  def fetch_all(limit: 1000)
    all = []
    cursor = 0
    loop do
      res = Net::HTTP.get_response(URI("#{SAMPLES_API}?limit=#{limit}&cursor=#{cursor}"))
      raise "HTTP #{res.code} for samples API" unless res.is_a?(Net::HTTPSuccess)

      body = JSON.parse(res.body)
      rows = body['data'] || []
      all.concat(rows)
      nc = body.dig('meta', 'pagination', 'next_cursor')
      break if rows.empty? || rows.size < limit || nc.nil? || nc == cursor

      cursor = nc
    end
    all
  end

  def lit(s)
    e = s.to_s.gsub(/[\\"\n\r\t]/) do |c|
      { "\\" => '\\\\', '"' => '\\"', "\n" => '\\n', "\r" => '\\r', "\t" => '\\t' }[c]
    end
    %("#{e}")
  end

  # 対象 ttl の「生成ブロックより前」で定義済みの jogo_pop: コード集合
  def defined_pop_codes(ttl)
    head = ttl.split(MARK_BEGIN, 2).first.to_s
    head.scan(/^jogo_pop:(\w+)\s+a\b/).flatten.to_set
  end

  def build_block(samples)
    pop2gpop = {}
    gpops = Set.new
    samples.each do |s|
      pop2gpop[s['popname']] = s['gpopname']
      gpops << s['gpopname']
    end

    @existing ||= Set.new
    out = +''
    out << MARK_BEGIN << "\n"
    out << "# 自動生成: #{SAMPLES_API} を全件取得。総サンプル数 #{samples.size}。手で編集しないこと。\n"
    out << "# (上の集団定義は templates/population.ttl 由来。以下は未定義集団の stub と sample)\n\n"

    # 未定義 super-population の stub
    stub_g = gpops.reject { |g| @existing.include?(g) }.sort
    unless stub_g.empty?
      out << "# samples が参照するが未定義の super-population (stub)\n"
      stub_g.each do |g|
        out << "jogo_pop:#{g} a jogo:SuperPopulation ; dcterms:identifier #{lit(g)} ; rdfs:label #{lit(g)} .\n"
      end
      out << "\n"
    end

    # 未定義 population の stub
    stub_p = pop2gpop.keys.reject { |p| @existing.include?(p) }.sort
    unless stub_p.empty?
      out << "# samples が参照するが未定義の population (stub)\n"
      stub_p.each do |p|
        g = pop2gpop[p]
        sup = (g && g != p) ? " ; jogo:superPopulation jogo_pop:#{g}" : ''
        out << "jogo_pop:#{p} a jogo:Population ; dcterms:identifier #{lit(p)} ; " \
               "rdfs:label #{lit(p)}#{sup} .\n"
      end
      out << "\n"
    end

    # samples
    out << "# samples (#{samples.size})\n"
    samples.sort_by { |s| s['sampleid'] }.each do |s|
      t = ['a jogo:Sample',
           "dcterms:identifier #{lit(s['sampleid'])}",
           "jogo:population jogo_pop:#{s['popname']}"]
      (s['groupnames'] || []).each { |gn| t << "jogo:group #{lit(gn)}" }
      out << "jogo_sample:#{s['sampleid']}\n    " << t.join(" ;\n    ") << " .\n"
    end
    out << MARK_END << "\n"
    out
  end

  # template(集団 ttl) に sample ブロックを足して全文を返す。
  # 既存 sample ブロックがあれば除去し、jogo_sample prefix を保証。
  def merge_into(ttl, block)
    ttl = ttl.sub(/#{Regexp.escape(MARK_BEGIN)}.*?#{Regexp.escape(MARK_END)}\n?/m, '').rstrip
    unless ttl.include?('@prefix jogo_sample:')
      ttl = ttl.sub(/(^@prefix .*\.\s*\n)(?!@prefix)/m) { "#{Regexp.last_match(1)}#{SAMPLE_PREFIX}\n" }
    end
    "#{ttl}\n\n#{block}"
  end
end

# === main ====================================================================
if $PROGRAM_NAME == __FILE__
  opts = {
    template: File.expand_path('../templates/population.ttl', __dir__), # 集団 ttl(入力)
    out: File.expand_path('../output/samples.ttl', __dir__)             # 集団 + sample(出力)
  }
  OptionParser.new do |o|
    o.banner = 'Usage: ruby bin/samples2ttl.rb [--template PATH] [--out PATH]'
    o.on('--template PATH', '集団 ttl (既定: templates/population.ttl)') { |x| opts[:template] = x }
    o.on('--out PATH', '出力 (既定: output/samples.ttl, "-" で標準出力)') { |x| opts[:out] = x }
  end.parse!(ARGV)

  warn 'fetching samples ...'
  samples = Samples.fetch_all
  warn "fetched #{samples.size} samples"
  samples.select! { |s| !((s['groupnames'] || []) & KEEP_GROUPS).empty? }
  warn "kept #{samples.size} samples in groups #{KEEP_GROUPS.join(' / ')}"

  # template の集団に sample を足して output/samples.ttl に出力 (集団+sample の完全版)。
  template = File.read(opts[:template])
  Samples.instance_variable_set(:@existing, Samples.defined_pop_codes(template))
  result = Samples.merge_into(template, Samples.build_block(samples))

  if opts[:out] == '-'
    $stdout.write(result)
  else
    require 'fileutils'
    FileUtils.mkdir_p(File.dirname(opts[:out])) # ディレクトリ自動作成
    File.write(opts[:out], result)
    warn "wrote #{opts[:out]} (#{result.bytesize} bytes; #{samples.size} samples)"
  end
end
